// Абстрактное синтаксическое дерево для G-кода

use std::fmt;

#[derive(Debug, Clone, PartialEq)]
/// Основное перечисление для представления различных типов операторов в программе G-кода
/// 
/// Каждый вариант перечисления представляет собой определенный тип команды или инструкции,
/// которые могут встречаться в программах ЧПУ. Использование перечисления позволяет структурировать
/// и классифицировать все возможные элементы программы.
#[derive(Debug, Clone, PartialEq)]
pub enum Statement {
    /// Движение (G-коды): G0, G1, G2 и т.д.
    Motion(MotionStatement),
    /// Вспомогательные функции (M-коды): M3, M5, M6 и т.д.
    Misc(MiscStatement),
    /// Установка координат осей: X, Y, Z, A, B, C и т.д.
    Axis(AxisStatement),
    /// Комментарий в программе: текст в скобках или после символа ;
    Comment(CommentStatement),
    /// Специфические или неизвестные конструкции, которые не нужно изменять
    /// 
    /// Это позволяет сохранять оригинальный формат специфических команд,
    /// не пытаясь их интерпретировать, что важно для совместимости с различными станками.
    Raw(String),
}

#[derive(Debug, Clone, PartialEq)]
/// Представляет команду движения в программе G-кода (G-коды)
/// 
/// G-коды управляют движением инструмента: быстрое позиционирование, линейная интерполяция,
/// круговая интерполяция и другие типы движения. Эта структура содержит информацию о
/// конкретной команде движения.
#[derive(Debug, Clone, PartialEq)]
pub struct MotionStatement {
    /// Номер G-кода (0 для G0, 1 для G1 и т.д.)
    pub code: i32,
    /// Флаг быстрого перемещения (true для G0, false для G1 и других)
    /// 
    /// Это позволяет легко определить тип движения без проверки номера кода.
    pub rapid: bool,
}

#[derive(Debug, Clone, PartialEq)]
/// Представляет вспомогательную функцию в программе G-кода (M-коды)
/// 
/// M-коды управляют вспомогательными функциями станка: запуск/останов шпинделя,
/// смена инструмента, включение охлаждения и другие операции, не связанные напрямую с движением.
#[derive(Debug, Clone, PartialEq)]
pub struct MiscStatement {
    /// Номер M-кода (3 для M3, 5 для M5 и т.д.)
    pub code: i32,
}

#[derive(Debug, Clone, PartialEq)]
/// Представляет установку координаты для конкретной оси в программе G-кода
/// 
/// В программах ЧПУ используются различные оси (X, Y, Z, A, B, C и др.),
/// и каждая ось может быть установлена на определенное значение координаты.
/// Эта структура хранит информацию о такой установке.
#[derive(Debug, Clone, PartialEq)]
pub struct AxisStatement {
    /// Название оси (X, Y, Z, A, B, C и т.д.)
    pub axis: String,
    /// Значение координаты для этой оси
    pub value: f64,
}

#[derive(Debug, Clone, PartialEq)]
/// Представляет комментарий в программе G-кода
/// 
/// Комментарии используются для документирования программы, объяснения отдельных операций
/// или временного отключения строк кода. Они могут быть заключены в скобки или следовать
/// после символа ; и игнорируются при выполнении программы на станке.
#[derive(Debug, Clone, PartialEq)]
pub struct CommentStatement {
    /// Текст комментария без ограничивающих символов (скобок или ;)
    pub text: String,
}

/// Реализация форматирования для Statement, позволяющая преобразовать структуру в строку
/// 
/// Это необходимо для последующего вывода отформатированного G-кода после обработки.
/// Каждый тип оператора преобразуется в соответствующую текстовую форму в соответствии
/// со стандартами G-кода.
impl fmt::Display for Statement {
    fn fmt(&self, f: &mut fmt::Formatter) -> fmt::Result {
        match self {
            Statement::Motion(m) => write!(f, "G{}", m.code),
            Statement::Misc(m) => write!(f, "M{}", m.code),
            Statement::Axis(a) => write!(f, "{}{}", a.axis, a.value),
            Statement::Comment(c) => write!(f, "({})", c.text),
            Statement::Raw(r) => write!(f, "{}", r),
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_statement_display() {
        let motion = Statement::Motion(MotionStatement { code: 0, rapid: true });
        let axis = Statement::Axis(AxisStatement { axis: "X".to_string(), value: 10.5 });
        let comment = Statement::Comment(CommentStatement { text: "Test move".to_string() });

        assert_eq!(motion.to_string(), "G0");
        assert_eq!(axis.to_string(), "X10.5");
        assert_eq!(comment.to_string(), "(Test move)");
    }
}